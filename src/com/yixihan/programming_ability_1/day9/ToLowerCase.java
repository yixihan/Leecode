package com.yixihan.programming_ability_1.day9;

/**
 * 给你一个字符串 s ，将该字符串中的大写字母转换成相同的小写字母，返回新的字符串。
 *
 * 输入：s = "Hello"
 * 输出："hello"
 *
 * 输入：s = "here"
 * 输出："here"
 *
 * 输入：s = "LOVELY"
 * 输出："lovely"
 *
 * @author : yixihan
 * @create : 2022-03-19-11:54
 */
public class ToLowerCase {
    public String toLowerCase(String s) {
        return s.toLowerCase ();
    }
}
